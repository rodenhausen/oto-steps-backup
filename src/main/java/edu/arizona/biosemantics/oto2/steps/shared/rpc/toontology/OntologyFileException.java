public class OntologyFileException extends Exception {

	public OntologyFileException() { }
	
	public OntologyFileException(String message) {
        super(message);
    }
	
	public OntologyFileException(String message, Throwable cause) {
        super(message);
    }
	
	public OntologyFileException(Throwable cause) {
		super(cause);
	}
	


}
